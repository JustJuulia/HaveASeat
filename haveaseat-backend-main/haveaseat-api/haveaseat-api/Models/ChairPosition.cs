namespace haveaseat.Models;
/// <summary>
/// This enum represents the possible positions of a chair.
/// </summary>
public enum ChairPosition
{
    /// <summary>
    /// The chair is positioned at the top.
    /// </summary>
    TOP,

    /// <summary>
    /// The chair is positioned on the right.
    /// </summary>
    RIGHT,

    /// <summary>
    /// The chair is positioned at the bottom.
    /// </summary>
    BOTTOM,

    /// <summary>
    /// The chair is positioned on the left.
    /// </summary>
    LEFT
}